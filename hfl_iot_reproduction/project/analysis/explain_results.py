def _collect_artifacts() -> list[tuple[str, int, str]]:
    rows = []
    for path in sorted(OUT.glob("*")):
        if path.is_file():
            rows.append((path.name, int(path.stat().st_size), _artifact_description(path.name)))
    return rows


def _pick_cols(df: pd.DataFrame):
    score_col = None
    error_col = None
    if "train_score" in df.columns:
        score_col = "train_score"
    elif "train_acc" in df.columns:
        score_col = "train_acc"
    elif "val_score" in df.columns:
        score_col = "val_score"
    elif "val_acc" in df.columns:
        score_col = "val_acc"

    if "train_rmse" in df.columns:
        error_col = "train_rmse"
    elif "train_loss" in df.columns:
        error_col = "train_loss"
    elif "val_rmse" in df.columns:
        error_col = "val_rmse"
    elif "val_loss" in df.columns:
        error_col = "val_loss"
    return score_col, error_col


def _build_iot_summary(iot_df: pd.DataFrame):
    rows = []
    by_iot = {}
    if iot_df.empty:
        return rows, by_iot

    score_col, error_col = _pick_cols(iot_df)

    for iot_id, sub in sorted(iot_df.groupby("iot"), key=lambda kv: str(kv[0])):
        sub = sub.sort_values("round")
        rounds = int(sub["round"].max()) if "round" in sub.columns and sub["round"].notna().any() else len(sub)

        score = sub[score_col].dropna() if score_col in sub.columns else pd.Series(dtype=float)
        error = sub[error_col].dropna() if error_col in sub.columns else pd.Series(dtype=float)

        first_score = float(score.iloc[0]) if len(score) > 0 else None
        last_score = float(score.iloc[-1]) if len(score) > 0 else None
        best_score = float(score.max()) if len(score) > 0 else None
        last_error = float(error.iloc[-1]) if len(error) > 0 else None
        best_error = float(error.min()) if len(error) > 0 else None

        if len(score) >= 10:
            trend = float(score.tail(5).mean() - score.head(5).mean())
        elif len(score) >= 2:
            trend = float(score.iloc[-1] - score.iloc[0])
        else:
            trend = None

        by_iot[str(iot_id)] = {
            "rounds": rounds,
            "first_score": first_score,
            "last_score": last_score,
            "best_score": best_score,
            "last_error": last_error,
            "best_error": best_error,
            "trend": trend,
        }

        rows.append(
            [
                str(iot_id),
                _fmt_int(rounds),
                _fmt_float(first_score),
                _fmt_float(last_score),
                _fmt_float(best_score),
                _fmt_float(last_error),
                _fmt_float(best_error),
                _fmt_float(trend),
            ]
        )

    return rows, by_iot


def _build_agg_summary(cloud_df: pd.DataFrame):
    rows = []
    by_target = {}
    if cloud_df.empty:
        return rows, by_target

    groups = cloud_df.groupby("target") if "target" in cloud_df.columns else [("global", cloud_df)]
    for target, sub in sorted(groups, key=lambda kv: str(kv[0])):
        sub = sub.sort_values("_ts")
        win = sub["window"].dropna() if "window" in sub.columns else pd.Series(dtype=float)
        p = sub["pactual"].dropna() if "pactual" in sub.columns else pd.Series(dtype=float)
        contrib = sub["edges"].dropna() if "edges" in sub.columns else pd.Series(dtype=float)
        b = sub["buf"].dropna() if "buf" in sub.columns else pd.Series(dtype=float)

        window_start = float(win.iloc[0]) if len(win) > 0 else None
        window_last = float(win.iloc[-1]) if len(win) > 0 else None
        p_half_ratio = float((p == 0.5).mean()) if len(p) > 0 else None
        p_full_ratio = float((p == 1.0).mean()) if len(p) > 0 else None
        contrib_mean = float(contrib.mean()) if len(contrib) > 0 else None
        buf_mean = float(b.mean()) if len(b) > 0 else None

        by_target[str(target)] = {
            "window_start": window_start,
            "window_last": window_last,
            "window_min": float(win.min()) if len(win) > 0 else None,
            "window_max": float(win.max()) if len(win) > 0 else None,
            "p_half_ratio": p_half_ratio,
            "p_full_ratio": p_full_ratio,
            "contributors_mean": contrib_mean,
            "buf_mean": buf_mean,
        }

        rows.append(
            [
                str(target),
                _fmt_float(window_start, 1),
                _fmt_float(window_last, 1),
                _fmt_float(by_target[str(target)]["window_min"], 1),
                _fmt_float(by_target[str(target)]["window_max"], 1),
                _fmt_pct(p_half_ratio),
                _fmt_pct(p_full_ratio),
                _fmt_float(contrib_mean),
                _fmt_float(buf_mean, 2),
            ]
        )

    return rows, by_target


def _build_cloud_summary(cloud_df: pd.DataFrame):
    if cloud_df.empty:
        return None

    sub = cloud_df.copy()
    if "round" in sub.columns:
        sub = sub.sort_values("round")
    else:
        sub = sub.sort_values("_ts")

    edges = sub["edges"].dropna() if "edges" in sub.columns else pd.Series(dtype=float)
    beta = sub["beta_cloud"].dropna() if "beta_cloud" in sub.columns else pd.Series(dtype=float)

    duration_s = _duration_seconds(sub)
    updates_per_min = (len(sub) / (duration_s / 60.0)) if duration_s and duration_s > 0 else None

    return {
        "rows": len(sub),
        "round_min": int(sub["round"].min()) if "round" in sub.columns and sub["round"].notna().any() else None,
        "round_max": int(sub["round"].max()) if "round" in sub.columns and sub["round"].notna().any() else None,
        "edges_min": float(edges.min()) if len(edges) > 0 else None,
        "edges_mean": float(edges.mean()) if len(edges) > 0 else None,
        "edges_max": float(edges.max()) if len(edges) > 0 else None,
        "beta_mode": float(beta.mode().iloc[0]) if len(beta) > 0 else None,
        "updates_per_min": updates_per_min,
        "duration_s": duration_s,
    }


def _plot_interpretation_lines(iot_map: dict, agg_map: dict, cloud_summary: dict | None, artifact_names: list[str]) -> list[str]:
    lines = []
    for name in artifact_names:
        m = re.match(r"^iot_(.+)_score\.png$", name)
        if m:
            iot_id = m.group(1)
            info = iot_map.get(iot_id, {})
            lines.append(
                f"- `{name}`: evolution of IoT `{iot_id}` training score across local rounds; "
                f"last={_fmt_float(info.get('last_score'))}, best={_fmt_float(info.get('best_score'))}."
            )
            continue

        m = re.match(r"^iot_(.+)_error\.png$", name)
        if m:
            iot_id = m.group(1)
            info = iot_map.get(iot_id, {})
            lines.append(
                f"- `{name}`: evolution of IoT `{iot_id}` training error (RMSE or MSE); "
                f"last={_fmt_float(info.get('last_error'))}, best={_fmt_float(info.get('best_error'))}."
            )
            continue

        m = re.match(r"^iot_(.+)_acc\.png$", name)
        if m:
            iot_id = m.group(1)
            lines.append(
                f"- `{name}`: evolution of IoT `{iot_id}` accuracy across local rounds (classification)."
            )
            continue

        if name == "cloud_window_pactual.png":
            info = agg_map.get("global", {}) if "global" in agg_map else next(iter(agg_map.values()), {})
            lines.append(
                f"- `{name}`: cloud aggregation window and participation (pactual); "
                f"window { _fmt_float(info.get('window_start'), 1) } -> { _fmt_float(info.get('window_last'), 1) }, "
                f"pactual=0.5 in {_fmt_pct(info.get('p_half_ratio'))} of observations."
            )
            continue

        if name == "cloud_iots.png":
            if cloud_summary:
                lines.append(
                    f"- `{name}`: number of IoTs seen by cloud per aggregation round; "
                    f"min/mean/max={_fmt_float(cloud_summary.get('edges_min'), 1)}/"
                    f"{_fmt_float(cloud_summary.get('edges_mean'), 2)}/"
                    f"{_fmt_float(cloud_summary.get('edges_max'), 1)}."
                )
            else:
                lines.append(f"- `{name}`: number of IoTs seen by cloud per aggregation round.")
            continue

        if name == "cloud_global_rmse.png":
            lines.append(f"- `{name}`: global RMSE evolution across cloud aggregation rounds.")
            continue

        if name == "cloud_global_score.png":
            lines.append(f"- `{name}`: global score evolution across cloud aggregation rounds.")
            continue

        if name == "cloud_global_r2.png":
            lines.append(f"- `{name}`: global R2 evolution across cloud aggregation rounds.")
            continue

        if name == "cloud_global_mape.png":
            lines.append(f"- `{name}`: global MAPE evolution across cloud aggregation rounds.")
        if name == "cloud_global_acc.png":
            lines.append(f"- `{name}`: global accuracy evolution across cloud aggregation rounds.")
        if name.startswith("cloud_global_rmse_") and name.endswith(".png"):
            lines.append(f"- `{name}`: target-specific global RMSE across cloud aggregation rounds.")
        if name.startswith("cloud_global_score_") and name.endswith(".png"):
            lines.append(f"- `{name}`: target-specific global score across cloud aggregation rounds.")
        if name.startswith("cloud_global_r2_") and name.endswith(".png"):
            lines.append(f"- `{name}`: target-specific global R2 across cloud aggregation rounds.")
        if name.startswith("cloud_global_mape_") and name.endswith(".png"):
            lines.append(f"- `{name}`: target-specific global MAPE across cloud aggregation rounds.")
        if name.startswith("iot_") and name.endswith("_distill_rmse.png"):
            lines.append(f"- `{name}`: distillation alignment between student and teacher on that IoT.")
            continue

        if name.startswith("cloud_global_") and name.endswith("_rmse.png"):
            lines.append(f"- `{name}`: per-target RMSE evolution at cloud level.")
            continue

        if name.startswith("cloud_global_") and name.endswith("_score.png"):
            lines.append(f"- `{name}`: per-target score evolution at cloud level.")
            continue

        if name.startswith("cloud_global_") and name.endswith("_acc.png"):
            lines.append(f"- `{name}`: per-target accuracy evolution at cloud level.")
            continue

    return lines


def generate_results_explanation(df: pd.DataFrame | None = None) -> Path:
    if df is None:
        if not CSV.exists():
            raise FileNotFoundError(f"Missing {CSV}. Run extract_metrics first.")
        df = pd.read_csv(CSV)

    metrics = df[df["type"] == "metric"].copy() if "type" in df.columns else pd.DataFrame()
    events = df[df["type"] == "event"].copy() if "type" in df.columns else pd.DataFrame()

    iot = metrics[metrics["file"].str.startswith("iot")] if not metrics.empty and "file" in metrics.columns else pd.DataFrame()
    cloud = metrics[metrics["file"].str.startswith("cloud")] if not metrics.empty and "file" in metrics.columns else pd.DataFrame()

    iot_rows, iot_map = _build_iot_summary(iot)
    agg_rows, agg_map = _build_agg_summary(cloud)
    cloud_summary = _build_cloud_summary(cloud)

    iot_rounds = [v["rounds"] for v in iot_map.values() if v.get("rounds") is not None]
    min_iot_round = min(iot_rounds) if iot_rounds else None
    max_iot_round = max(iot_rounds) if iot_rounds else None
    iot_round_gap = (max_iot_round - min_iot_round) if iot_rounds else None

    artifact_rows = _collect_artifacts()
    artifact_names = [row[0] for row in artifact_rows]
    plot_lines = _plot_interpretation_lines(iot_map, agg_map, cloud_summary, artifact_names)

    run_duration_s = _duration_seconds(metrics)
    generated_at = datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")

    report_lines = [
        "# Results Explanation (IoT -> Cloud)",
        "",
        f"Generated at: {generated_at}",
        "",
        "## App Context Used For Interpretation",
        "- IoT behavior comes from `project/iot/device.py`: each device trains locally, reports `train_score`/`train_rmse`/`train_loss` (and `val_*` when available), encrypts weights, and sends updates directly to the cloud.",
        "- IoT distillation (if enabled) uses a teacher model from the cloud; `distill_*` metrics reflect student vs teacher alignment.",
        "- Cloud behavior comes from `project/cloud/server.py`: centralized async aggregation `w = (alpha*w + beta*sum(w_i)) / (alpha + beta*|A|)` and adaptive window update `window = max(3, window + alpha_sw*(pdesired - pactual))`.",
        "- The cloud maintains per-target global models and optional teacher training on proxy data.",
        "- Analysis pipeline context comes from `project/analysis/extract_metrics.py`, `project/analysis/plot_curves.py`, and `project/analysis/summarize.py`.",
        "",
        "## Input Snapshot",
        f"- Source CSV: `{CSV.name}` with {len(df)} rows and columns: {', '.join(df.columns.tolist())}.",
        f"- Metric rows: {len(metrics)}; event rows: {len(events)}.",
        f"- Metric time span: {_fmt_ts(metrics['_ts'].min() if not metrics.empty else None)} to {_fmt_ts(metrics['_ts'].max() if not metrics.empty else None)}.",
        f"- Metric duration (approx): {_fmt_float(run_duration_s, 2)} seconds.",
        "",
        "## Output Artifacts",
        _md_table(
            ["Artifact", "Size (bytes)", "Meaning"],
            [[name, str(size), meaning] for (name, size, meaning) in artifact_rows],
        ),
        "",
        "## IoT Layer Results",
        _md_table(
            ["IoT", "Rounds", "First Score", "Last Score", "Best Score", "Last Error", "Best Error", "Score Trend"],
            iot_rows,
        ),
        "",
        f"- Fastest IoT completed {_fmt_int(max_iot_round)} rounds; slowest completed {_fmt_int(min_iot_round)} rounds; gap={_fmt_int(iot_round_gap)} rounds.",
        "- This gap is expected in this app because updates are asynchronous and each IoT proceeds independently (no global per-round barrier).",
        "",
        "## Cloud Aggregation (Async)",
        _md_table(
            [
                "Target",
                "Window Start",
                "Window Last",
                "Window Min",
                "Window Max",
                "pactual=0.5",
                "pactual=1.0",
                "Contrib. Mean",
                "Buffer Mean",
            ],
            agg_rows,
        ),
        "",
        "- The adaptive window shrinks when participation exceeds the desired level and grows when participation is low, reflecting async arrival dynamics.",
        "- Frequent `pactual=0.5` indicates many aggregation steps were triggered by only a subset of IoTs.",
        "",
        "## Cloud Layer Results",
    ]

    if cloud_summary is None:
        report_lines.extend(
            [
                "- No cloud metrics were found in `metrics_all.csv`.",
                "",
            ]
        )
    else:
        report_lines.extend(
            [
                f"- Cloud metric rows: {_fmt_int(cloud_summary.get('rows'))}.",
                f"- Cloud rounds observed: {_fmt_int(cloud_summary.get('round_min'))} to {_fmt_int(cloud_summary.get('round_max'))}.",
                f"- Contributing IoTs (min/mean/max): {_fmt_float(cloud_summary.get('edges_min'), 1)}/"
                f"{_fmt_float(cloud_summary.get('edges_mean'), 2)}/"
                f"{_fmt_float(cloud_summary.get('edges_max'), 1)}.",
                f"- Observed `beta_cloud` mode: {_fmt_float(cloud_summary.get('beta_mode'), 3)}.",
                f"- Cloud aggregation frequency (approx): {_fmt_float(cloud_summary.get('updates_per_min'), 2)} updates/min.",
                "",
                "- Cloud rounds can exceed individual IoT local rounds in this implementation because cloud increments on each aggregation window, not on synchronized global barriers.",
                "",
            ]
        )

    report_lines.extend(
        [
            "## Figure-by-Figure Reading Guide",
        ]
    )
    if plot_lines:
        report_lines.extend(plot_lines)
    else:
        report_lines.append("- No PNG plots found in outputs.")

    report_lines.extend(
        [
            "",
            "## Important Interpretation Notes",
            "- These outputs expose local training metrics (IoT) and cloud aggregation dynamics (window, participation, contributors).",
            "- Cloud logs include `global_score`/`global_rmse` when available; if missing, global quality should be inferred from IoT trajectories.",
            "- For classification tasks, local accuracy appears as `train_acc`/`val_acc` and cloud accuracy as `global_acc` (per target when enabled).",
            "- Multiple `start` events in logs typically indicate services restarted and appended to the same log files; this can inflate total event counts while metric trends remain usable.",
        ]
    )

    OUT.mkdir(parents=True, exist_ok=True)
    tmp = REPORT.with_suffix(REPORT.suffix + ".tmp")
    tmp.write_text("\n".join(report_lines) + "\n", encoding="utf-8")
    tmp.replace(REPORT)
    return REPORT


def main():
    if not CSV.exists():
        print("Run extract_metrics first.")
        return
    df = pd.read_csv(CSV)
    out = generate_results_explanation(df)
    print(f"[analysis] wrote {out}")


if __name__ == "__main__":
    main()
